const MESSAGES: Record<Locale, Messages> = {
  en: {},
  ko: KO,
};

export function normalizeLocale(locale?: string): Locale {
  const normalized = locale?.toLowerCase();
  if (SUPPORTED_LANGUAGES.some((language) => language.value === normalized)) {
    return normalized as Locale;
  }
  return DEFAULT_LOCALE;
}

export function translate(text: string, locale?: string) {
  const normalized = normalizeLocale(locale);
  return MESSAGES[normalized][text as MessageKey] ?? text;
}

export function useI18n() {
  const language = useSettingsStore((s) => s.settings?.language);
  const locale = normalizeLocale(language);
  return useCallback((text: string) => translate(text, locale), [locale]);
}

export function trOptions<T extends { label: string }>(options: T[], t: (text: string) => string): T[] {
  return options.map((option) => ({ ...option, label: t(option.label) }));
}
